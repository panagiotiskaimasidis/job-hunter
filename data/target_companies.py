def company_names() -> list[str]:
    """Return just the company names — used as the LinkedIn company filter."""
    return [c[0] for c in TARGET_COMPANIES]


def by_sector(sector: str) -> list[str]:
    """Return company names in a given sector code."""
    return [c[0] for c in TARGET_COMPANIES if c[1] == sector]


def _norm(s: str) -> str:
    """Normalise a company name for fuzzy matching."""
    import re
    s = s.lower()
    s = re.sub(r"&", "and", s)
    s = re.sub(r"[^a-z0-9 ]", " ", s)
    # Strip common corporate suffixes/words
    for w in ("group", "international", "technologies", "technology", "company",
              "corporation", "corp", "industries", "plc", "ag", "sa", "se", "inc",
              "gmbh", "ltd", "limited", "the"):
        s = re.sub(rf"\b{w}\b", "", s)
    return re.sub(r"\s+", " ", s).strip()


# Pre-computed normalised set for O(1) membership checks
_NORMALISED = {_norm(c[0]) for c in TARGET_COMPANIES}


def is_target_company(company: str) -> bool:
    """
    True if a scraped company name matches one of our target companies.
    Used to guarantee these companies pass the quality-tier gate — a job at
    Siemens or Airbus should never be wrongly classified as an unknown SME.
    """
    if not company:
        return False
    n = _norm(company)
    if not n:
        return False
    if n in _NORMALISED:
        return True
    # Substring match either direction (e.g. "Siemens Energy GmbH" ↔ "Siemens Energy")
    for target in _NORMALISED:
        if target and (target in n or n in target) and abs(len(target) - len(n)) <= 8:
            return True
    return False
